package model;

public class SupplyModel {

	int store_code;
	int supply_order_code;
	String supplier_name;
	String supply_name;
	int supply_order_price;
	int supply_qty;
	String supply_order_date;
	
	public int getStore_code() {
		return store_code;
	}
	public void setStore_code(int store_code) {
		this.store_code = store_code;
	}
	public int getSupply_order_code() {
		return supply_order_code;
	}
	public void setSupply_order_code(int supply_order_code) {
		this.supply_order_code = supply_order_code;
	}
	public String getSupplier_name() {
		return supplier_name;
	}
	public void setSupplier_name(String supplier_name) {
		this.supplier_name = supplier_name;
	}
	public String getSupply_name() {
		return supply_name;
	}
	public void setSupply_name(String supply_name) {
		this.supply_name = supply_name;
	}
	public int getSupply_order_price() {
		return supply_order_price;
	}
	public void setSupply_order_price(int supply_order_price) {
		this.supply_order_price = supply_order_price;
	}
	public int getSupply_qty() {
		return supply_qty;
	}
	public void setSupply_qty(int supply_qty) {
		this.supply_qty = supply_qty;
	}
	public String getSupply_order_date() {
		return supply_order_date;
	}
	public void setSupply_order_date(String supply_order_date) {
		this.supply_order_date = supply_order_date;
	}
}
